#!/usr/bin/env python3
"""Train the reef NCA — same architecture and protocol as cube3D's NCA3D
(nca_3d_v1.py / BIGCUBE_V2.ipynb), reimplemented in PyTorch-CPU:

  - 28-channel state [structure, 20 hidden, 7 class beliefs]
  - perceive: Conv3d 3x3x3 masked to the 7-point stencil, relu
  - dmodel:   1x1x1 conv 84 -> relu -> 84 -> 27, second layer zeros-init
  - residual tanh update, stochastic fire rate 0.5, living mask on structure
  - loss: cross-entropy on the last 7 channels, masked to living cells,
    after a random number of rollout steps
  - kernel mask re-applied after every optimizer step (reset_diag_kernel)

The corpus is generated by reef/js/species.js (node scripts/…) so the label
source of truth is the JS generator shared with the judge site and the world.

Usage:
  python3 train_reef.py --corpus reef_corpus.json --updates 2500 \
      --out reef_run/ [--resume reef_run/ckpt.pt] [--drop rejected.json]
"""
import argparse, base64, json, math, os, time
import numpy as np
import torch
import torch.nn as nn
import torch.nn.functional as F

GRID, CH, PC, NC = 15, 28, 84, 7
NCELLS = GRID ** 3


def unpack(b64):
    bytes_ = np.frombuffer(base64.b64decode(b64), dtype=np.uint8)
    bits = np.unpackbits(bytes_)[:NCELLS]
    return bits.reshape(GRID, GRID, GRID).astype(np.float32)


def load_corpus(path, drop_path=None):
    d = json.load(open(path))
    per, species = d['per'], d['species']
    shapes = np.stack([unpack(s) for s in d['shapes']])
    labels = np.array(d['labels'], dtype=np.int64)
    ids = [f"{species[l].lower()}:{i % per}" for i, l in zip(range(len(labels)), labels)]
    keep = np.ones(len(labels), dtype=bool)
    if drop_path and os.path.exists(drop_path):
        rejected = set(json.load(open(drop_path)))
        keep = np.array([i not in rejected for i in ids])
        print(f"crowd-rejected specimens dropped: {(~keep).sum()}")
    # held-out split: last 20 seeds of each species (never dropped, never trained)
    seeds = np.array([i % per for i in range(len(labels))])
    test = seeds >= per - 20
    train = keep & ~test
    return shapes, labels, train, test, species


class NCA(nn.Module):
    def __init__(self):
        super().__init__()
        self.perceive = nn.Conv3d(CH, PC, 3, padding=1)
        self.c1 = nn.Conv3d(PC, PC, 1)
        self.c2 = nn.Conv3d(PC, CH - 1, 1)
        nn.init.zeros_(self.c2.weight)
        nn.init.zeros_(self.c2.bias)
        mask = torch.zeros(1, 1, 3, 3, 3)
        for k in [(1, 1, 1), (0, 1, 1), (2, 1, 1), (1, 0, 1), (1, 2, 1), (1, 1, 0), (1, 1, 2)]:
            mask[0, 0, k[0], k[1], k[2]] = 1
        self.register_buffer('mask', mask)
        self.reset_diag_kernel()

    @torch.no_grad()
    def reset_diag_kernel(self):
        self.perceive.weight.mul_(self.mask)

    def step(self, x, fire=0.5, generator=None):
        gray = x[:, :1]
        y = F.relu(self.perceive(x))
        h = F.relu(self.c1(y))
        upd = torch.tanh(self.c2(h))
        rnd = torch.rand(x.shape[0], 1, GRID, GRID, GRID, generator=generator)
        m = ((rnd <= fire) & (gray > 0.1)).float()
        return torch.cat([gray, x[:, 1:] + upd * m], dim=1)

    def rollout(self, structure, steps, fire=0.5, generator=None):
        x = torch.cat([structure.unsqueeze(1),
                       torch.zeros(structure.shape[0], CH - 1, GRID, GRID, GRID)], dim=1)
        for _ in range(steps):
            x = self.step(x, fire, generator)
        return x

    def classify(self, x):
        return x[:, -NC:]


def evaluate(model, shapes, labels, idx, seed=0):
    g = torch.Generator().manual_seed(seed)
    cell_accs, majority_ok = [], 0
    conf = np.zeros((NC, NC), dtype=int)
    with torch.no_grad():
        for k in range(0, len(idx), 16):
            batch = idx[k:k + 16]
            s = torch.from_numpy(shapes[batch])
            x = model.rollout(s, 90, generator=g)
            pred = model.classify(x).argmax(1)  # (B, D, H, W)
            for bi, i in enumerate(batch):
                m = s[bi] > 0
                p = pred[bi][m]
                acc = (p == labels[i]).float().mean().item()
                cell_accs.append(acc)
                maj = int(torch.bincount(p, minlength=NC).argmax())
                conf[labels[i], maj] += 1
                if maj == labels[i]:
                    majority_ok += 1
    return float(np.mean(cell_accs)), majority_ok / len(idx), conf


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument('--corpus', required=True)
    ap.add_argument('--updates', type=int, default=2500)
    ap.add_argument('--batch', type=int, default=8)
    ap.add_argument('--lr', type=float, default=2e-4)
    ap.add_argument('--steps-lo', type=int, default=56)
    ap.add_argument('--steps-hi', type=int, default=88)
    ap.add_argument('--out', default='reef_run')
    ap.add_argument('--resume', default=None)
    ap.add_argument('--drop', default=None, help='JSON list of crowd-rejected specimen ids')
    ap.add_argument('--seed', type=int, default=7)
    ap.add_argument('--eval-every', type=int, default=250)
    ap.add_argument('--ckpt-every', type=int, default=100)
    args = ap.parse_args()

    os.makedirs(args.out, exist_ok=True)
    torch.manual_seed(args.seed)
    np.random.seed(args.seed)

    shapes, labels, train_mask, test_mask, species = load_corpus(args.corpus, args.drop)
    train_idx = np.where(train_mask)[0]
    test_idx = np.where(test_mask)[0]
    print(f"train {len(train_idx)} · held-out {len(test_idx)} · classes {species}")

    model = NCA()
    opt = torch.optim.Adam(model.parameters(), lr=args.lr)
    start = 0
    if args.resume and os.path.exists(args.resume):
        ck = torch.load(args.resume, weights_only=False)
        model.load_state_dict(ck['model'])
        opt.load_state_dict(ck['opt'])
        start = ck['update']
        print(f"resumed at update {start}")

    labels_t = torch.from_numpy(labels)
    t0 = time.time()
    for u in range(start, args.updates):
        batch = np.random.choice(train_idx, args.batch, replace=False)
        s = torch.from_numpy(shapes[batch])
        y = labels_t[batch]
        steps = np.random.randint(args.steps_lo, args.steps_hi + 1)
        x = model.rollout(s, steps)
        logits = model.classify(x)  # (B, 7, D, H, W)
        target = y.view(-1, 1, 1, 1).expand(-1, GRID, GRID, GRID)
        ce = F.cross_entropy(logits, target, reduction='none')  # (B, D, H, W)
        m = s > 0
        loss = (ce * m).sum() / m.sum()

        opt.zero_grad()
        loss.backward()
        torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
        opt.step()
        model.reset_diag_kernel()

        with torch.no_grad():
            pred = logits.argmax(1)
            acc = ((pred == target) & m).sum().item() / m.sum().item()
        dt = time.time() - t0
        print(f"u {u + 1}/{args.updates} loss {loss.item():.4f} acc {acc:.3f} "
              f"steps {steps} {dt / (u - start + 1):.1f}s/u", flush=True)

        if (u + 1) % args.ckpt_every == 0 or u + 1 == args.updates:
            torch.save({'model': model.state_dict(), 'opt': opt.state_dict(), 'update': u + 1},
                       os.path.join(args.out, 'ckpt.pt'))
        if (u + 1) % args.eval_every == 0 or u + 1 == args.updates:
            ca, ma, conf = evaluate(model, shapes, labels, test_idx)
            print(f"EVAL u{u + 1}: held-out cell acc {ca:.3f} majority {ma:.3f}", flush=True)
            print(conf, flush=True)
            with open(os.path.join(args.out, 'eval.json'), 'w') as f:
                json.dump({'update': u + 1, 'cell_acc': ca, 'majority_acc': ma,
                           'confusion': conf.tolist(), 'species': species}, f, indent=1)

    # ---- export weights in the golem/tjs codec order --------------------
    W = model.perceive.weight.detach().numpy()   # (84, 28, 3, 3, 3)
    kd = lambda k0, k1, k2: np.ascontiguousarray(W[:, :, k0, k1, k2].T)  # (28, 84) in->out
    order = [kd(1, 1, 1), kd(0, 1, 1), kd(2, 1, 1), kd(1, 0, 1), kd(1, 2, 1), kd(1, 1, 0), kd(1, 1, 2)]
    blob = np.concatenate([a.reshape(-1) for a in order] + [
        model.perceive.bias.detach().numpy(),
        model.c1.weight.detach().numpy()[:, :, 0, 0, 0].T.reshape(-1),  # (84,84) in->out
        model.c1.bias.detach().numpy(),
        model.c2.weight.detach().numpy()[:, :, 0, 0, 0].T.reshape(-1),  # (84,27) in->out
        model.c2.bias.detach().numpy(),
    ]).astype('<f4')
    assert blob.size == 25983, blob.size
    b64 = base64.b64encode(blob.tobytes()).decode()
    with open(os.path.join(args.out, 'weights-reef.js'), 'w') as f:
        f.write('// Reef NCA weights — same architecture as the cube3D firmware network,\n')
        f.write('// trained by reef/train/train_reef.py on the procedural species corpus\n')
        f.write('// (reef/js/species.js). NOT the original authors\' weights.\n')
        f.write(f'export const REEF_SPECIES = {json.dumps(species)};\n')
        f.write(f'export const WEIGHTS_B64 = "{b64}";\n')
    print(f"exported {os.path.join(args.out, 'weights-reef.js')} ({blob.size} floats)")

    # golden vectors for the JS engine (deterministic: fire=1)
    golden = []
    for i in test_idx[:3]:
        s = torch.from_numpy(shapes[i:i + 1])
        x = torch.cat([s.unsqueeze(1), torch.zeros(1, CH - 1, GRID, GRID, GRID)], dim=1)
        with torch.no_grad():
            for _ in range(20):
                gray = x[:, :1]
                y = F.relu(model.perceive(x))
                h = F.relu(model.c1(y))
                upd = torch.tanh(model.c2(h))
                m = (gray > 0.1).float()
                x = torch.cat([gray, x[:, 1:] + upd * m], dim=1)
        mask = s[0] > 0
        mean_logits = [round(float(x[0, CH - NC + k][mask].mean()), 5) for k in range(NC)]
        golden.append({'index': int(i), 'label': int(labels[i]), 'steps': 20,
                       'meanLogits': mean_logits, 'liveCells': int(mask.sum())})
    with open(os.path.join(args.out, 'golden-reef.json'), 'w') as f:
        json.dump(golden, f, indent=1)
    print('golden vectors written')


if __name__ == '__main__':
    main()
