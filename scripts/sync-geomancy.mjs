// AUTO-SYNC: read/geomancy/data.js (canonical, pure Fludd) → clock/lib/geomancy-meanings.js
// (the casting table's anchor). Run `node scripts/sync-geomancy.mjs` after editing data.js.
//
// We also DERIVE, here, the few quantities Fludd's own rules let us compute from his text —
// so the reading engine can apply the Regulæ without re-encoding lore:
//   • a figure's MODE (movable / fixed / common) from its zodiac sign — licensed by Regula IV
//     ("figuræ significent … mobilitatem, fixitatem, communitatem secundum naturam signi").
//   • a figure's STRENGTH (strong / weak / middling) from Fludd's Cap. III classification (p. 39).
//   • each house's SIGN and MODE from Cap. II (house 1 = Aries movable, … 12 = Pisces common).
// data.js stays the pure transcription; the derivations (per Fludd's mappings) live here.
import { GEOMANCY } from '../read/geomancy/data.js';
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const key = rows => rows.map(r => r === 1 ? '1' : '0').join('');   // 1 = single/active

// Fludd Cap. II / Regula IV: the quality of each zodiac sign.
const SIGNS = ['Aries','Taurus','Gemini','Cancer','Leo','Virgo','Libra','Scorpio','Sagittarius','Capricorn','Aquarius','Pisces'];
const MODE_OF = sign => ['movable','fixed','common'][SIGNS.indexOf(sign) % 3] || 'common';
// Fludd Cap. III (p. 39): the strong, the weak, the middling.
const STRENGTH = {
  strong:   ['Fortuna Major','Acquisitio','Laetitia','Puer','Albus','Caput Draconis'],
  weak:     ['Fortuna Minor','Amissio','Tristitia','Puella','Rubeus','Cauda Draconis'],
  middling: ['Populus','Via','Coniunctio','Carcer'],
};
const strengthOf = la => Object.keys(STRENGTH).find(k => STRENGTH[k].includes(la)) || 'middling';
const primarySign = z => (z || '').split(/\s*&\s*|,/)[0].trim();

const entries = GEOMANCY.figures.map(f => {
  const o = { la:f.la, en:f.en, planet:f.planet, nature:f.nature, sig:f.sig,
              zodiac:f.zodiac, mode:MODE_OF(primarySign(f.zodiac)), strength:strengthOf(f.la) };
  if (f.domus) o.domus = f.domus;                 // the per-shield-place significations {la,en}
  if (f.physiognomy) o.physiognomy = f.physiognomy;
  return `  '${key(f.rows)}': ${JSON.stringify(o)},`;
}).join('\n');

const houses = GEOMANCY.houses.map(h => {
  const sign = SIGNS[h.n - 1];
  return `  ${JSON.stringify({ n:h.n, name:h.name, title:h.title, matter:h.matter, domain:h.domain,
    sign, mode:MODE_OF(sign), la:h.la, en:h.en })},`;
}).join('\n');

const extras = (GEOMANCY.houseExtras || []).map(x =>
  `  ${JSON.stringify({ place:x.place, name:x.name, la:x.la, en:x.en })},`
).join('\n');

// L7 — the perfection doctrine (Liber III, Cap. VI), so the engine can show Fludd's own
// definition for each connection it detects.
const perfection = JSON.stringify(GEOMANCY.fludd.perfection || null);
// the geomantic aspects (companion tract), to colour the manner of the outcome.
const aspects = JSON.stringify(GEOMANCY.fludd.aspects || null);
// Cap. IV — the querent's figure doubling into each house.
const doubling = JSON.stringify(GEOMANCY.fludd.doubling || null);

const out =
`// AUTO-GENERATED by scripts/sync-geomancy.mjs from read/geomancy/data.js — do not edit by hand.
// The casting table's anchor: figure significations (keyed by active-bit pattern, Fire→Earth),
// the twelve houses, and the three further places (Witnesses + Judge). Each figure also carries
// its DERIVED mode + strength, and each house its sign + mode (see the sync header for how).
// read/geomancy/data.js is canonical; rerun \`node scripts/sync-geomancy.mjs\` after edits.
export const MEANINGS = {
${entries}
};
export const HOUSES = [
${houses}
];
export const HOUSE_EXTRAS = [
${extras}
];
export const PERFECTION = ${perfection};
export const ASPECTS = ${aspects};
export const DOUBLING = ${doubling};
`;
writeFileSync(join(ROOT, 'clock/lib/geomancy-meanings.js'), out);
console.log('wrote clock/lib/geomancy-meanings.js — figures:', GEOMANCY.figures.length,
  '· houses:', GEOMANCY.houses.length, '· extras:', (GEOMANCY.houseExtras||[]).length);
