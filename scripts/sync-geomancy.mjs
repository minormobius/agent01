// sync-geomancy.mjs — regenerate clock/lib/geomancy-meanings.js (the casting table's
// anchor copy of the figure significations) from read/geomancy/data.js, the canonical
// home. The two surfaces (g.mino.mobi, read.mino.mobi) deploy from different dirs and
// can't share a served file, so the significations are mirrored into geocast here.
// Run after editing the redaction:  node scripts/sync-geomancy.mjs
import { GEOMANCY } from '../read/geomancy/data.js';
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const key = rows => rows.map(r => r === 1 ? '1' : '0').join('');   // 1 = single/active

const entries = GEOMANCY.figures.map(f =>
  `  '${key(f.rows)}': ${JSON.stringify({ la:f.la, en:f.en, planet:f.planet, nature:f.nature, sig:f.sig })},`
).join('\n');
const houses = GEOMANCY.houses.map(h =>
  `  ${JSON.stringify({ n:h.n, name:h.name, title:h.title, matter:h.matter, domain:h.domain })},`
).join('\n');

const out =
`// AUTO-GENERATED by scripts/sync-geomancy.mjs from read/geomancy/data.js — do not edit by hand.
// The casting table's anchor copy of the figure significations (keyed by the figure's
// active-bit pattern, Fire→Earth) and the twelve houses. read/geomancy/data.js is
// canonical; rerun \`node scripts/sync-geomancy.mjs\` after edits.
export const MEANINGS = {
${entries}
};
export const HOUSES = [
${houses}
];
`;
writeFileSync(join(ROOT, 'clock/lib/geomancy-meanings.js'), out);
console.log('wrote clock/lib/geomancy-meanings.js — figures:', GEOMANCY.figures.length, '· houses:', GEOMANCY.houses.length);
