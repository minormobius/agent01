#!/usr/bin/env node
/**
 * Generate io/sites.json — the registry the StumbleUpon portal randomizes over.
 *
 * Source of truth is the `var P = [...]` PROJECTS array in the root index.html
 * (the same array generate-search-catalog.mjs and generate-og-card.mjs parse).
 * We re-emit it in the portal's domain-first shape so the constellation can grow
 * beyond mino.mobi: additional `constellations[]` entries (other domains/repos)
 * are appended by hand or by sibling repos' CI — this generator only owns the
 * mino.mobi entry and leaves any others in place.
 *
 * Usage:
 *   node scripts/generate-sites-json.mjs            # write io/sites.json
 *   node scripts/generate-sites-json.mjs --dry      # print, don't write
 */
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const INDEX_HTML = join(ROOT, 'index.html');
const OUT = join(ROOT, 'io', 'sites.json');

const MINO_DOMAIN = 'mino.mobi';
const MINO_REPO = 'minormobius/agent01';

// Hosts excluded from the stumble portal because they hard-block iframe
// embedding (X-Frame-Options: DENY), which makes them blank in the wrapper.
// Keyed by HOST (not entry name) because the block is per-origin: org's
// children (pm, wave.mino.mobi, wiki) live on other origins and frame fine.
// TODO: re-include once they allowlist `frame-ancestors https://io.mino.mobi`
// in their response headers (poll worker headers + org). Tracked in io/DESIGN.md §7.2.
const FRAME_BLOCKED_HOSTS = new Set([
  'poll.mino.mobi', // X-Frame-Options: DENY
  'org.mino.mobi',  // X-Frame-Options: DENY
]);

function hostOf(url) {
  try { return new URL(url).host; } catch { return ''; }
}

// Live endpoints that exist on disk + serve 200 but aren't (yet) in index.html's
// PROJECTS array. Added here so the portal can reach them without a drive-by edit
// to the landing page's curated array.
// TODO: fold these into the canonical `var P` array in index.html (then the
// search catalog + OG card pick them up too) and delete from this list.
const SUPPLEMENTAL = []; // folded into index.html var P (2026-06-04); empty to avoid duplicates

function parseProjects(html) {
  const start = html.indexOf('var P = [');
  if (start === -1) throw new Error('Could not find `var P = [` in index.html');
  const end = html.indexOf('];', start);
  const block = html.slice(start, end);
  // { n:'poll', u:'https://poll.mino.mobi', c:'bluesky', k:75, a:'warm', p:'photo' }
  // Match the 5 required fields; tolerate any trailing fields (t:, b:, p:) in any
  // order before the closing brace, and pull `parent` out of that tail if present.
  // (var P entries carry t:/b: date fields the old regex didn't account for, which
  // silently dropped every dated entry.)
  const re = /\{\s*n:'([^']+)',\s*u:'([^']+)',\s*c:'([^']+)',\s*k:(\d+),\s*a:'([^']+)'([^}]*)\}/g;
  const out = [];
  let m;
  while ((m = re.exec(block)) !== null) {
    const [, name, url, category, k, heat, rest] = m;
    const pm = /p:'([^']+)'/.exec(rest);
    out.push({ name, url, category, weight: Number(k), heat, parent: pm ? pm[1] : null });
  }
  return out;
}

function buildMinoConstellation(projects) {
  // Include EVERY destination — top-level sites AND buried sub-pages. For a
  // stumble tool, landing on deep/obscure pages is the whole point. We only
  // drop entries whose HOST hard-blocks iframe embedding.
  const all = [...projects, ...SUPPLEMENTAL.map((s) => ({ ...s, parent: null }))];
  const sites = all
    .filter((p) => !FRAME_BLOCKED_HOSTS.has(hostOf(p.url)))
    .map((p) => ({
      url: p.url,
      name: p.name,
      tags: [p.category],
      weight: p.weight,
      heat: p.heat,
      ...(p.parent ? { parent: p.parent } : {}),
    }));
  return { domain: MINO_DOMAIN, repo: MINO_REPO, sites };
}

function main() {
  const dry = process.argv.includes('--dry');
  const html = readFileSync(INDEX_HTML, 'utf8');
  const projects = parseProjects(html);
  const mino = buildMinoConstellation(projects);

  // Preserve any non-mino constellation entries a human/other repo added.
  let others = [];
  if (existsSync(OUT)) {
    try {
      const prev = JSON.parse(readFileSync(OUT, 'utf8'));
      others = (prev.constellations || []).filter((c) => c.domain !== MINO_DOMAIN);
    } catch { /* regenerate from scratch */ }
  }

  const doc = {
    generated: new Date().toISOString(),
    note: 'Generated by scripts/generate-sites-json.mjs from index.html PROJECTS. Add other domains as new constellations[] entries.',
    constellations: [mino, ...others],
  };

  const json = JSON.stringify(doc, null, 2) + '\n';
  if (dry) {
    process.stdout.write(json);
    console.error(`\n(dry run) ${mino.sites.length} mino sites, ${others.length} other constellation(s)`);
    return;
  }
  writeFileSync(OUT, json);
  console.log(`Wrote ${OUT}: ${mino.sites.length} mino sites, ${others.length} other constellation(s)`);
}

main();
