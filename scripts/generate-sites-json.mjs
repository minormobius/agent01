#!/usr/bin/env node
/**
 * Generate io/sites.json — the registry the StumbleUpon portal randomizes over.
 *
 * Source of truth is the `var P = [...]` PROJECTS array in the root index.html
 * (the same array generate-search-catalog.mjs and generate-og-card.mjs parse).
 * We re-emit it in the portal's domain-first shape so the constellation can grow
 * beyond mino.mobi: additional `constellations[]` entries (other domains/repos)
 * are appended by hand or by sibling repos' CI — this generator only owns the
 * mino.mobi entry and leaves any others in place.
 *
 * Usage:
 *   node scripts/generate-sites-json.mjs            # write io/sites.json
 *   node scripts/generate-sites-json.mjs --dry      # print, don't write
 */
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const INDEX_HTML = join(ROOT, 'index.html');
const OUT = join(ROOT, 'io', 'sites.json');

const MINO_DOMAIN = 'mino.mobi';
const MINO_REPO = 'minormobius/agent01';

function parseProjects(html) {
  const start = html.indexOf('var P = [');
  if (start === -1) throw new Error('Could not find `var P = [` in index.html');
  const end = html.indexOf('];', start);
  const block = html.slice(start, end);
  // { n:'poll', u:'https://poll.mino.mobi', c:'bluesky', k:75, a:'warm', p:'photo' }
  const re = /\{\s*n:'([^']+)',\s*u:'([^']+)',\s*c:'([^']+)',\s*k:(\d+),\s*a:'([^']+)'(?:,\s*p:'([^']+)')?\s*\}/g;
  const out = [];
  let m;
  while ((m = re.exec(block)) !== null) {
    const [, name, url, category, k, heat, parent] = m;
    out.push({ name, url, category, weight: Number(k), heat, parent: parent || null });
  }
  return out;
}

function buildMinoConstellation(projects) {
  // Top-level sites only (skip `p`-having sub-entries so the portal lands on
  // distinct destinations, not many paths under one app). Weight by commit count.
  const sites = projects
    .filter((p) => !p.parent)
    .map((p) => ({
      url: p.url,
      name: p.name,
      tags: [p.category],
      weight: p.weight,
      heat: p.heat,
    }));
  return { domain: MINO_DOMAIN, repo: MINO_REPO, sites };
}

function main() {
  const dry = process.argv.includes('--dry');
  const html = readFileSync(INDEX_HTML, 'utf8');
  const projects = parseProjects(html);
  const mino = buildMinoConstellation(projects);

  // Preserve any non-mino constellation entries a human/other repo added.
  let others = [];
  if (existsSync(OUT)) {
    try {
      const prev = JSON.parse(readFileSync(OUT, 'utf8'));
      others = (prev.constellations || []).filter((c) => c.domain !== MINO_DOMAIN);
    } catch { /* regenerate from scratch */ }
  }

  const doc = {
    generated: new Date().toISOString(),
    note: 'Generated by scripts/generate-sites-json.mjs from index.html PROJECTS. Add other domains as new constellations[] entries.',
    constellations: [mino, ...others],
  };

  const json = JSON.stringify(doc, null, 2) + '\n';
  if (dry) {
    process.stdout.write(json);
    console.error(`\n(dry run) ${mino.sites.length} mino sites, ${others.length} other constellation(s)`);
    return;
  }
  writeFileSync(OUT, json);
  console.log(`Wrote ${OUT}: ${mino.sites.length} mino sites, ${others.length} other constellation(s)`);
}

main();
