// Bundle the civ engine for the browser (client-side compute).
//
//   node scripts/build-civ-engine.mjs
//
// Produces civ/lib/civ-engine.js — a self-contained ES module bundling the deterministic sim
// (engine + signals + qd + config + chronicle) so the browser can run a civ locally, bit-for-bit
// identical to the civ.mino.mobi API (same chronicleHash). It's a COMMITTED build artifact: the
// civ deploy is plain `wrangler deploy` with no build step, so regenerate + commit this after
// changing anything under mappa/civ/. Requires esbuild (`npm i -D esbuild`, or npx esbuild).
import { build } from 'esbuild';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
await build({
  entryPoints: [resolve(root, 'civ/lib/civ-engine.src.mjs')],
  outfile: resolve(root, 'civ/lib/civ-engine.js'),
  bundle: true,
  format: 'esm',
  target: 'es2022',
  legalComments: 'none',
  banner: { js: '// GENERATED by scripts/build-civ-engine.mjs — do not edit. Bundles mappa/civ/* for client-side compute.' },
});
console.log('✓ built civ/lib/civ-engine.js');
