/* build-pendragon-motifs.cjs
 *
 * Derives the FULL cross-corpus motif set for the Pendragon "Compare" graph
 * from every tale's own motifs.js, unioned by Thompson code. Each tale's
 * curated crosswalk (read/pendragon/crosswalk.js) stays the hand-written
 * comparative subset; THIS file is the complete picture — every motif each
 * tale carries, pulled toward the tales that carry it and coloured by class.
 *
 * Output: read/pendragon/motifs-all.js  (window.PENDRAGON.motifsAll)
 * Re-run:  node scripts/build-pendragon-motifs.cjs
 */
const fs = require("fs");
const path = require("path");

const ROOT = path.join(__dirname, "..", "read");
const TALES = ["culhwch", "pwyll", "branwen", "manawydan", "math", "owain", "orfeo", "gawain", "vitamerlini"];

global.window = {};
for (const id of TALES) require(path.join(ROOT, id, "motifs.js"));

const CLASS_NAME = {
  A: "Mythological", B: "Animals", C: "Tabu / interdiction", D: "Magic", E: "The dead",
  F: "Marvels & the Otherworld", G: "Ogres & giants", H: "Tests & tasks", J: "Wise & foolish",
  K: "Deceptions", L: "Reversals of fortune", M: "Ordaining the future", N: "Chance & fate",
  P: "Society", Q: "Rewards & punishments", R: "Captives & fugitives", S: "Unnatural cruelty",
  T: "Sex, love & marriage", V: "Religion", W: "Traits of character", Z: "Formulas & symbols",
};

const byCode = {};
for (const id of TALES) {
  const list = (((global.window[id.toUpperCase()] || {}).motifs) || {}).list || [];
  for (const m of list) {
    const code = String(m.code || m.cls || "?").trim();
    if (!byCode[code]) byCode[code] = { code, name: m.name || code, cls: (m.cls || (code[0] || "Z")) };
    if ((!byCode[code].name || byCode[code].name === code) && m.name) byCode[code].name = m.name;
    byCode[code][id] = { passages: m.passages || [], conf: m.conf || "med", note: m.gloss || "" };
  }
}

const rows = Object.values(byCode).map((r) => {
  const hits = TALES.filter((id) => r[id]);
  const cn = CLASS_NAME[r.cls] || ("Class " + r.cls);
  r.gloss = `Thompson <strong>${r.cls}</strong> · ${cn} — carried by ${hits.length} of the nine tale${hits.length === 1 ? "" : "s"}.`;
  r._hits = hits.length;
  return r;
});

// Sort: most-shared first, then by class order, then by code — so the shared
// backbone leads and the single-tale leaves trail (same spirit as the crosswalk).
const CLASS_ORDER = "ABCDEFGHJKLMNPQRSTVWZ".split("");
rows.sort((a, b) =>
  b._hits - a._hits ||
  CLASS_ORDER.indexOf(a.cls) - CLASS_ORDER.indexOf(b.cls) ||
  a.code.localeCompare(b.code));
rows.forEach((r) => delete r._hits);

const out =
  "/* GENERATED by scripts/build-pendragon-motifs.cjs — do not edit by hand.\n" +
  "   The full cross-corpus motif union (every tale's motifs.js, merged by\n" +
  "   Thompson code) for the Pendragon Compare graph's \"All corpus\" view.\n" +
  "   Re-run the script after changing any tale's motifs. */\n" +
  "window.PENDRAGON = window.PENDRAGON || {};\n" +
  "window.PENDRAGON.motifsAll = " + JSON.stringify(rows) + ";\n" +
  "window.PENDRAGON.motifClassNames = " + JSON.stringify(CLASS_NAME) + ";\n";

const dest = path.join(ROOT, "pendragon", "motifs-all.js");
fs.writeFileSync(dest, out);
console.log("wrote", path.relative(path.join(__dirname, ".."), dest));
console.log("  motif codes:", rows.length, "| classes:", [...new Set(rows.map((r) => r.cls))].sort().join(" "));
console.log("  file size:", (out.length / 1024).toFixed(1), "KB");
