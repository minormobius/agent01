// sync-zhouyi.mjs — regenerate clock/lib/zhouyi.js (the oracle's anchor copy of
// the open translation) from read/iching/data.js, the canonical home.
//
// The two surfaces (g.mino.mobi, read.mino.mobi) deploy from different dirs and
// can't share a served file, so the translation is mirrored into the oracle here.
// We carry the FULL apparatus the oracle surfaces under Zhu Xi's rules:
//   · the Judgment (卦辭) — for the primary, and for the relating hexagram
//   · all six line-texts (爻辭) — for whichever line(s) a cast surfaces
//   · the use-line (用九 / 用六) — for an all-moving cast of Hexagram 1 or 2
// (We omit `figure` — the oracle already holds the King Wen binaries — and the
// hexagram-level prose `note`, since the oracle composes its own Image line.)
//
// Run after editing the translation:  node scripts/sync-zhouyi.mjs
import { ZHOUYI } from '../read/iching/data.js';
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const hex = ZHOUYI.hexagrams;
const nums = Object.keys(hex).map(Number).sort((a, b) => a - b);

// pretty-print one hexagram's served subset, indented to sit under its key
const indent = (s, pad) => s.split('\n').map((l, i) => (i ? pad + l : l)).join('\n');
const entries = nums.map(n => {
  const h = hex[n];
  const served = { name: h.name, judgment: h.judgment, lines: h.lines };
  if (h.useLine) served.useLine = h.useLine;
  return `  ${n}: ${indent(JSON.stringify(served, null, 2), '  ')},`;
}).join('\n');

const out =
`// AUTO-GENERATED by scripts/sync-zhouyi.mjs from read/iching/data.js — do not edit by hand.
// The oracle's anchor copy of our open Zhouyi translation. The two surfaces
// (g.mino.mobi, read.mino.mobi) deploy from different directories and can't share
// a served file, so the translation is mirrored here for g.mino.mobi/yijing.
// Each entry carries { name, judgment, lines[6] (, useLine) } — the text a cast
// surfaces under Zhu Xi's rules. read/iching/data.js is canonical; rerun
// \`node scripts/sync-zhouyi.mjs\` after edits.
export const ZHOUYI = {
${entries}
};
`;

writeFileSync(join(ROOT, 'clock/lib/zhouyi.js'), out);
console.log('wrote clock/lib/zhouyi.js — hexagrams:', nums.length, '(' + nums[0] + '–' + nums[nums.length - 1] + ')');
