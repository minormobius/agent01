// chat — conversation practice (robo trainer)
//
// Surface: chat.mino.mobi
//   /            landing page
//   /robo/       practice against an AI partner, scored on the rubric
//   /docs/       the theory + rubric-development view
//
// API:
//   GET  /api/chat/health                 liveness + bindings
//   GET  /api/chat/config                 rubric + personas + scenarios (shared module)
//   POST /api/chat/turn    {persona,scenario,history,playerCount?}  -> { reply }
//   POST /api/chat/score   {persona,scenario,transcript}            -> { total, band, axes[] }
//
// Everything conversational is generated by Workers AI. The rubric, personas,
// and scenarios are imported verbatim from lib/rubric.js — the same file the
// two browser pages import — so the thing you're scored on and the thing the
// docs describe can never drift apart.

import {
  RUBRIC,
  PERSONAS,
  SCENARIOS,
  UNITS,
  CHALLENGES,
  personaById,
  scenarioById,
  scoreBand,
  challengeById,
  challengeStars,
} from './lib/rubric.js';

const MODEL = '@cf/meta/llama-3.3-70b-instruct-fp8-fast';

const CORS = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Methods': 'GET,POST,OPTIONS',
  'Access-Control-Allow-Headers': 'Content-Type',
};

export default {
  async fetch(request, env) {
    const url = new URL(request.url);
    const { pathname } = url;

    if (request.method === 'OPTIONS') return new Response(null, { headers: CORS });

    try {
      if (pathname === '/api/chat/health') {
        return json({
          ok: true,
          version: 'chat-v2-challenges',
          rubric: RUBRIC.version,
          challenges: CHALLENGES.length,
          model: MODEL,
          bindings: { ai: !!env.AI, assets: !!env.ASSETS },
        });
      }

      if (pathname === '/api/chat/config') {
        return json({ rubric: RUBRIC, personas: PERSONAS, scenarios: SCENARIOS, units: UNITS, challenges: CHALLENGES });
      }

      if (pathname === '/api/chat/turn' && request.method === 'POST') {
        return botTurn(request, env);
      }

      if (pathname === '/api/chat/score' && request.method === 'POST') {
        return scoreTranscript(request, env);
      }

      if (pathname.startsWith('/api/')) return json({ error: 'not found' }, 404);
    } catch (e) {
      return json({ error: String((e && e.message) || e) }, 500);
    }

    // Everything else is a static asset (landing / robo / docs / lib).
    return env.ASSETS.fetch(request);
  },
};

// ── Bot turn ─────────────────────────────────────────────────────────────────
// The AI plays the chosen persona inside the chosen scenario, replying to the
// running transcript. Kept deliberately short — this is real-time conversation,
// not an essay exchange.
async function botTurn(request, env) {
  const body = await safeJson(request);
  const persona = personaById(body.persona);
  const scenario = scenarioById(body.scenario);
  const history = Array.isArray(body.history) ? body.history.slice(-24) : [];
  const players = clampInt(body.playerCount, 2, 4, 2);

  const groupNote =
    players > 2
      ? `This is a group conversation with ${players} people. Do not hog the floor; sometimes address the group, sometimes one person.`
      : 'This is a one-on-one conversation.';

  const system = [
    `You are role-playing a conversation partner so a human can practice real-time conversation skills.`,
    `SETTING: ${scenario.setup}`,
    `YOUR CHARACTER — "${persona.name}": ${persona.system}`,
    groupNote,
    `Rules: Reply ONLY as your character, in first person. 1-3 sentences, like real speech — no stage directions, no narration, no emoji unless natural. Never break character or mention that this is practice. Never evaluate or coach the human; that happens separately.`,
  ].join('\n');

  // history items are { role: 'user'|'assistant', content }
  const messages = [{ role: 'system', content: system }];
  for (const m of history) {
    if (!m || !m.content) continue;
    messages.push({
      role: m.role === 'assistant' ? 'assistant' : 'user',
      content: String(m.content).slice(0, 1200),
    });
  }
  // If the transcript is empty, seed the character's opener rather than calling the model.
  if (history.length === 0) {
    return json({ reply: persona.opener });
  }

  const out = await env.AI.run(MODEL, { messages, max_tokens: 220, temperature: 0.9 });
  let reply = (out && (out.response || out.result)) || '';
  reply = cleanReply(String(reply));
  if (!reply) reply = '…hm, say more?';
  return json({ reply });
}

// ── AI judge: score the transcript against the rubric ────────────────────────
async function scoreTranscript(request, env) {
  const body = await safeJson(request);
  const challenge = body.challengeId ? challengeById(body.challengeId) : null;
  // In challenge mode the persona/scenario come from the challenge, so the
  // objective and the role-play can't be spoofed by the client.
  const persona = personaById(challenge ? challenge.persona : body.persona);
  const scenario = scenarioById(challenge ? challenge.scenario : body.scenario);
  const transcript = Array.isArray(body.transcript) ? body.transcript : [];

  // Render the transcript with speaker labels the judge can reason over.
  const rendered = transcript
    .filter((m) => m && m.content)
    .map((m) => `${m.role === 'assistant' ? persona.name : 'YOU'}: ${String(m.content).trim()}`)
    .join('\n');

  if (!rendered || transcript.filter((m) => m.role === 'user').length < 2) {
    return json({ error: 'transcript too short to score', need: 'at least 2 of your turns' }, 400);
  }

  const axisSpec = RUBRIC.axes
    .map(
      (a) =>
        `- ${a.key} ("${a.label}", 0-${a.max}): ${a.theory} ${a.one_liner} ` +
        `Reward: ${a.look_for.join('; ')}. Penalize: ${a.avoid.join('; ')}.`,
    )
    .join('\n');

  const objLine = challenge
    ? `\nCHALLENGE OBJECTIVE (evaluate strictly): ${challenge.objective}\nDecide objective_met (true/false) and give a one-sentence objective_note citing the specific evidence.`
    : '';
  const shape = challenge
    ? `{"axes":[{"key":"relevance","score":0,"evidence":"","tip":""}, ...one per axis in order...],"objective_met":true,"objective_note":"","summary":"..."}`
    : `{"axes":[{"key":"relevance","score":0,"evidence":"","tip":""}, ...one per axis in order...],"summary":"one or two sentences of overall feedback addressed to YOU"}`;

  const system = [
    `You are a rigorous but encouraging conversation coach. You score ONLY the person labeled "YOU" in the transcript — never the character "${persona.name}".`,
    `The setting was: ${scenario.setup}`,
    `Score these five axes independently:`,
    axisSpec,
    objLine,
    ``,
    `For each axis give an integer 0..max, a one-sentence "evidence" that quotes or paraphrases a specific moment from YOU, and a one-sentence "tip" for next time.`,
    `Be calibrated: an average competent conversation should land around 12-15 per axis, not maxed. Reserve high scores for genuinely skilled moves.`,
    `Respond with STRICT JSON only, no prose, no markdown fences, exactly this shape:`,
    shape,
  ].join('\n');

  const out = await env.AI.run(MODEL, {
    messages: [
      { role: 'system', content: system },
      { role: 'user', content: `TRANSCRIPT:\n${rendered}` },
    ],
    max_tokens: 900,
    temperature: 0.2,
  });

  const raw = (out && (out.response || out.result)) || '';
  const parsed = extractJson(raw);
  if (!parsed || !Array.isArray(parsed.axes)) {
    return json({ error: 'could not parse judge output', raw: String(raw).slice(0, 500) }, 502);
  }

  // Normalize against the rubric so the client always gets a complete, bounded
  // result even if the model drops or reorders an axis.
  const byKey = new Map(parsed.axes.map((a) => [a.key, a]));
  let total = 0;
  const axes = RUBRIC.axes.map((spec) => {
    const got = byKey.get(spec.key) || {};
    const score = clampInt(got.score, 0, spec.max, 0);
    total += score;
    return {
      key: spec.key,
      label: spec.label,
      max: spec.max,
      score,
      theory: spec.theory,
      source: spec.source,
      evidence: String(got.evidence || '').slice(0, 300),
      tip: String(got.tip || '').slice(0, 300),
    };
  });

  const band = scoreBand(total);
  const result = {
    total,
    band: band.title,
    band_note: band.note,
    summary: String(parsed.summary || '').slice(0, 600),
    axes,
    persona: persona.name,
    scenario: scenario.name,
  };

  if (challenge) {
    const focusAxis = axes.find((a) => a.key === challenge.focus) || axes[0];
    const objectiveMet = parsed.objective_met === true;
    const passed = objectiveMet && focusAxis.score >= challenge.pass;
    const stars = challengeStars({
      passed,
      objectiveMet,
      focusScore: focusAxis.score,
      focusPass: challenge.pass,
      total,
    });
    result.challenge = {
      id: challenge.id,
      title: challenge.title,
      focus: challenge.focus,
      focus_label: focusAxis.label,
      focus_score: focusAxis.score,
      focus_max: focusAxis.max,
      pass: challenge.pass,
      goal: challenge.goal,
    };
    result.objective_met = objectiveMet;
    result.objective_note = String(parsed.objective_note || '').slice(0, 300);
    result.passed = passed;
    result.stars = stars;
  }

  return json(result);
}

// ── helpers ──────────────────────────────────────────────────────────────────
function cleanReply(s) {
  let r = s.trim();
  // Strip a leading "Name:" the model sometimes prepends.
  r = r.replace(/^["“]?[A-Z][\w .'-]{0,24}:\s*/, '');
  // Strip surrounding quotes and stage directions in *asterisks* or (parens).
  r = r.replace(/\*[^*]*\*/g, '').replace(/^\s*\([^)]*\)\s*/, '');
  r = r.replace(/^["“](.*)["”]$/s, '$1');
  return r.trim();
}

function extractJson(s) {
  if (!s) return null;
  let t = String(s).trim();
  // Peel markdown code fences if present.
  const fence = t.match(/```(?:json)?\s*([\s\S]*?)```/i);
  if (fence) t = fence[1].trim();
  try {
    return JSON.parse(t);
  } catch {}
  // Fall back to the first {...} balanced-ish slice.
  const first = t.indexOf('{');
  const last = t.lastIndexOf('}');
  if (first !== -1 && last > first) {
    try {
      return JSON.parse(t.slice(first, last + 1));
    } catch {}
  }
  return null;
}

function clampInt(v, lo, hi, dflt) {
  const n = Math.round(Number(v));
  if (!Number.isFinite(n)) return dflt;
  return Math.max(lo, Math.min(hi, n));
}

async function safeJson(request) {
  try {
    return await request.json();
  } catch {
    return {};
  }
}

function json(obj, status = 200, extra = {}) {
  return new Response(JSON.stringify(obj), {
    status,
    headers: { 'Content-Type': 'application/json', ...CORS, ...extra },
  });
}
